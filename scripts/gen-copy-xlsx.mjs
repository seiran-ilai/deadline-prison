// ---------- OOXML 產生 ----------
const HEADERS = ['檔案', '行號', '區塊 / 情境', '目前文字', '建議修改(待填)']
const COLW = [30, 7, 24, 62, 40]

const esc = s => String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;')
const colName = n => { let s = ''; n++; while (n > 0) { const m = (n - 1) % 26; s = String.fromCharCode(65 + m) + s; n = (n - m - 1) / 26 } return s }

function sheetXml(rows) {
  const all = [HEADERS, ...rows]
  const cols = COLW.map((w, i) => `<col min="${i + 1}" max="${i + 1}" width="${w}" customWidth="1"/>`).join('')
  const body = all.map((r, ri) => {
    const cells = HEADERS.map((_, ci) => {
      const v = r[ci] ?? ''
      const ref = `${colName(ci)}${ri + 1}`
      const style = ri === 0 ? ' s="1"' : ''
      if (v === '') return `<c r="${ref}"${style}/>`
      return `<c r="${ref}"${style} t="inlineStr"><is><t xml:space="preserve">${esc(v)}</t></is></c>`
    }).join('')
    return `<row r="${ri + 1}">${cells}</row>`
  }).join('')
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<worksheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main">
<sheetViews><sheetView workbookViewId="0"><pane ySplit="1" topLeftCell="A2" activePane="bottomLeft" state="frozen"/></sheetView></sheetViews>
<cols>${cols}</cols>
<sheetData>${body}</sheetData>
</worksheet>`
}

